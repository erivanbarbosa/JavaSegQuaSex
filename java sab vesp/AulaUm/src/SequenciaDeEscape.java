// classe SequdenciaDeEscape
public class SequenciaDeEscape {
	// Método inicia a execução do programa java
	public static void main(String[] args) {
		// /n desce para proxima linha
		System.out.println("Linha de cima \nlinha de baixo");

		// \t aplica o espaço do tabulação do sistema
		System.out.println("\t -> tabulado");

		// \\ escreve o caractere "\"
		System.out.println("\\ Escreve a barra");

		System.out.println("achei muito \"legal\"");
	} // fim do método main
} // fim da classe SequenciaDeEscape
